  @Override
  public void runProcedure()
  {

    showAbundancePlot();
    filesIgnored = false;
    in_files = ar_input.getInputFiles();
    File out_dir = ar_input.getOutputDir();
    //StatusTracker statusTracker = new StatusTracker( AR_ProgressBar, lblStatus );
    if(!in_files.isEmpty())
    {
      lblStatus.setText( "Status: Processing..." );
      AR_ProgressBar.setMaximum( in_files.size() );
      Iterator<File> iterator = in_files.iterator();
      while ( iterator.hasNext() )
      {
        File theInput = iterator.next();
        //filename we will be creating in the filter tool:
        String singleOutPath = out_dir.getAbsolutePath() + DIR_SEPARATOR + FilenameUtils.removeExtension( theInput.getName() ) + "_AR.fa";
        File outputFile = new File( singleOutPath );
        //check for exist and do not overwrite unless told to:
        if ( outputFile.exists() )
        {
          if ( !this.ar_input.getOverwriteConfirm() )
          {
            filesIgnored = true;
            iterator.remove();
            LOGGER.log( Level.WARNING, "{0} not processed due to overwrite warning", outputFile.getName() );
            this.AR_ProgressBar.setMaximum( in_files.size() );
          }
        }

      }
      for ( File theInput : in_files )
      {
        AdaptorRemoverRunner adaptorRemoverRunner = new AdaptorRemoverRunner( this, showingAbundancePlots, streamMode );

        ar_output.addOutputTab( theInput.getName() );
        ar_output.setOutputLengthDistTabWaiting( theInput.getName(), true );
        ar_output.setOutputStatsTabWaiting( theInput.getName(), true );
        AdaptorRemoverParams ar_params = ar_input.getParams();
        adaptorRemoverRunner.runAdaptorRemover( theInput, out_dir, ar_params, null );
        ar_runners.put( theInput.getName(), adaptorRemoverRunner );
      }
      ar_output.revalidate();
      if ( filesIgnored )
      {
        JOptionPane.showMessageDialog( this,
          "Some files were ignored due to overwrite warnings" + LINE_SEPARATOR
          + "Re-run with force overwrite selected or choose an empty directory" + LINE_SEPARATOR
          + "Visit the log file for a list of all files that were not processed",
          "Output Warning",
          JOptionPane.ERROR_MESSAGE );
      }
    }
    else
    {
      JOptionPane.showMessageDialog( this,
          "No input files found",
          "Process Cancelled",
          JOptionPane.ERROR_MESSAGE );
    }
    
  }
  
