  @Override
  public void update()
  {    
    try
    {
      
      File out_dir = ar_input.getOutputDir();
      final ArrayList<String> historyWrite = new ArrayList<String>();
      boolean allComplete = true;
      for(final File theInput : in_files)
      {
        final AdaptorRemoverRunner getTrigger = ar_runners.get( theInput.getName() );
        if ( getTrigger.isComplete() )
        {
          historyWrite.add( out_dir.getAbsolutePath() + DIR_SEPARATOR + FilenameUtils.removeExtension( theInput.getName() ) + "_AR.fa" );

          lblStatus.setText( "Status: Completed: " + theInput.getName() );
          AR_ProgressBar.setValue( AR_ProgressBar.getValue() + 1 );
          ar_output.fillLengthDistTable( theInput.getName(), getTrigger.getLengths() );
          List<FilterStage> results = getTrigger.getResults();
          ar_output.fillExecutionStatsTable( theInput.getName(), getTrigger.getResults() );
          ar_output.setOutputLengthDistTabWaiting( theInput.getName(), false );
          ar_output.setOutputStatsTabWaiting( theInput.getName(), false );

        }
      }
      for(final File theInput : in_files)
      {
        AdaptorRemoverRunner getTrigger = ar_runners.get( theInput.getName() );
        if ( !getTrigger.isComplete() )
        {
          allComplete = false;
        }
      }
      //just give java a minute to think about swinging...
      Thread.sleep( 500 );
      if ( allComplete )
      {
        SwingUtilities.invokeLater( new Runnable()
        {
          @Override
          public void run()
          {
            lblStatus.setText( "Status: All Files Completed Successfully" );
            AR_ProgressBar.setValue( 0 );
            ar_input.addToHistory(historyWrite);
          }
        } );
      }
    }
    catch (Exception e)
    {
      showErrorDialog( e.getMessage() );
    }
  }
  
